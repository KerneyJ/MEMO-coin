// Overload >> for Ed25519Key
std::istream& operator>>(std::istream& is, Ed25519Key& hash) {
    // Assuming the input is in hexadecimal format
    for (auto& byte : hash) {
        int tmp;
        if (is >> std::hex >> tmp) {
            byte = static_cast<unsigned char>(tmp);
        } else {
            is.setstate(std::ios_base::failbit);
            return is;
        }
    }
    return is;
}

